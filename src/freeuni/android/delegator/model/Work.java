package freeuni.android.delegator.model;

public class Work {

	//Private variables
	private int workID;
	private String name;
	/**
	 * @return the workID
	 */
	public int getWorkID() {
		return workID;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param workID the workID to set
	 */
	public void setWorkID(int workID) {
		this.workID = workID;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
